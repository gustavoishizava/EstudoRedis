using WebApi.Services;

var builder = WebApplication.CreateBuilder(args);

// Add services to the container.

builder.Services.AddControllers();
// Learn more about configuring Swagger/OpenAPI at https://aka.ms/aspnetcore/swashbuckle
builder.Services.AddEndpointsApiExplorer();
builder.Services.AddSwaggerGen();

builder.Services.AddStackExchangeRedisCache(setup =>
{
    setup.InstanceName = "SampleInstance";
    setup.Configuration = builder.Configuration.GetConnectionString("Redis");
});

builder.Services.AddHttpClient<IProductService, ProductService>(config =>
{
    config.BaseAddress = new Uri(builder.Configuration.GetValue<string>("UrlApi"));
});

var app = builder.Build();

// Configure the HTTP request pipeline.
if (app.Environment.IsDevelopment())
{
    app.UseSwagger();
    app.UseSwaggerUI();
}

app.UseHttpsRedirection();

app.UseAuthorization();

app.MapControllers();

app.Run();
